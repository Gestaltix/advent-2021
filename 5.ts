export const filterNonCardinalVents = (vents: VentCoords[]): VentCoords[] =>
  vents.filter((vent) => {
    return vent.beginning.x === vent.end.x || vent.beginning.y === vent.end.y;
  });

export const mapVents = (vents: VentCoords[]): (number | undefined)[][] => {
  let result = new Array(1);
  for (const vent of vents) {
    for (let { x, y } of getVentPoints(vent)) {
      if (result.length <= x || result[x] === undefined) result[x] = [];
      if (result[x].length <= y || result[x][y] === undefined) result[x][y] = 1;
      else result[x][y] += 1;
    }
  }
  return result;
};

export const getOverlappingPoints = (
  ventMap: (number | undefined)[][]
): number => {
  let result = 0;
  for (const row of ventMap) {
    if (row !== undefined) {
      for (const point of row) {
        if (point !== undefined && point > 1) result++;
      }
    }
  }
  return result;
};

// Part 2
const getNumbersBetweenTwoPointsInc = (
  pointA: number,
  pointB: number
): number[] => {
  let result: number[] = new Array(Math.abs(pointA - pointB) + 1).fill(0);
  return result.map((num, i) => {
    if (pointA > pointB) return pointA - i;
    return pointA + i;
  });
};
const getVentPoints = (vent: VentCoords): { x: number; y: number }[] => {
  if (vent.beginning.x === vent.end.x) {
    return getNumbersBetweenTwoPointsInc(vent.beginning.y, vent.end.y).map(
      (y) => {
        return { x: vent.beginning.x, y };
      }
    );
  }
  if (vent.beginning.y === vent.end.y) {
    return getNumbersBetweenTwoPointsInc(vent.beginning.x, vent.end.x).map(
      (x) => {
        return { y: vent.beginning.y, x };
      }
    );
  }
  const xArr = getNumbersBetweenTwoPointsInc(vent.beginning.x, vent.end.x);
  const yArr = getNumbersBetweenTwoPointsInc(vent.beginning.y, vent.end.y);
  return xArr.map((x, i) => {
    return { x, y: yArr[i] };
  });
};
