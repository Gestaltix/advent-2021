// Part 1
export const travelInSub = (
  subCommands: SubmarineCommand[]
): SubmarineLocationData => {
  let subLocation: SubmarineLocationData = { horizontal: 0, depth: 0 };
  subCommands.forEach((command) => {
    switch (command.direction) {
      case "down":
        subLocation.depth += command.value;
        break;
      case "up":
        subLocation.depth -= command.value;
        break;
      case "forward":
        subLocation.horizontal += command.value;
        break;
      default:
        throw "Default Case Reached In Enum!!";
    }
  });
  return subLocation;
};

